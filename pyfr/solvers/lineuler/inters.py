class LinearEulerSupInflowBCInters(LinearEulerBaseBCInters):
    type = 'sup-in-fa'

    def __init__(self, be, lhs, elemap, cfgsect, cfg):
        super().__init__(be, lhs, elemap, cfgsect, cfg)

        self.c |= self._exp_opts(
            ['rho', 'p', 'u', 'v', 'w'][:self.ndims + 2], lhs
        )


class LinearEulerSupOutflowBCInters(LinearEulerBaseBCInters):
    type = 'sup-out-fn'
